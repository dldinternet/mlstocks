def print_title(s):
    """
    Print a string as a title with a strong underline

    Args:
        s: string to print as a title
    """
    print(s)
    print(len(s) * "=")
    print("")


def print_subtitle(s):
    """
    Print a string as a subtitle with an underline

    Args:
        s: string to print as a title
    """
    print(s)
    print(len(s) * "-")
    print("")
